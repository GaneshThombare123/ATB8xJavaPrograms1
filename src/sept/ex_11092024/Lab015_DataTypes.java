package sept.ex_11092024;

public class Lab015_DataTypes {
public static void main(String[] args){
// primitive data type
    byte b = 100;  // 8 bits, 1Byte
    short s = 128;  // 16 bits, 2 Byte
    int i = 1000;  //32 bits, 4 Byte
    long phone_number = 9668724423l;  //64 bits, 8 Byte
    char garde = 'A'; //a,b c,d,e....ASCII value assigned 16 bits, 2 Byte

    float f = 3.14f;  // 32 bits    4 Byte.
    double d = 180.974;   // 64 bits  8  Byte

    // non primitive data type-- (user defined)

    String name = "pramod";     // string - Bunch of characters(collections of char)
//    ARRAYS, CLASS -- user data types

}

}
